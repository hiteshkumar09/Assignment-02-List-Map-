//Q.1: Create a list of names and print all names using list.

void main() {
  // Creating a list of names
  List<String> names = ['Hiesh', 'Vijay', 'Doulat', 'Mahesh', 'Ali Raza'];

  // Printing all names using a for loop
  for (String name in names) {
    print(name);
  }
}